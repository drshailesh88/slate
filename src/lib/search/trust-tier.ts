const GOVERNMENT_SET = new Set(GOVERNMENT_DOMAINS);
const MAJOR_JOURNALISM_SET = new Set(MAJOR_JOURNALISM_DOMAINS);
const COMMUNITY_SET = new Set(COMMUNITY_DOMAINS);

function isGovernmentInstitutionalDomain(domain: string): boolean {
  return (
    GOVERNMENT_SET.has(domain) ||
    GOVERNMENT_SUFFIXES.some(
      (suffix) => domain === suffix.slice(1) || domain.endsWith(suffix)
    )
  );
}

export function getTrustTier(input: string | null | undefined): TrustTier {
  if (!input) return "other";

  const domain = normalizeDomain(input);
  if (!domain) return "other";

  if (isGovernmentInstitutionalDomain(domain)) {
    return "government";
  }

  if (MAJOR_JOURNALISM_SET.has(domain)) {
    return "major_journalism";
  }

  if (COMMUNITY_SET.has(domain)) {
    return "community";
  }

  return "other";
}
